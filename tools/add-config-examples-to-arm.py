import os
import pathlib
import sys


def main():
    parent_dir = os.path.dirname(os.path.realpath(os.path.abspath(sys.argv[0])))
    root_dir = f'{parent_dir}/..'
    all_jsons = sorted(pathlib.Path(root_dir).glob('doc/examples/**/*.json'))
    content = """
..
    File generated by "tools/add-config-examples-to-arm.py". Do not edit by hand.

.. _config-examples:

###############
Config Examples
###############

Kea is an open source implementation of the Dynamic Host Configuration
Protocol (DHCP) servers, developed and maintained by Internet Systems
Consortium (ISC).

This is a set of configuration examples for Kea version |release|.

.. toctree::
    :numbered:
    :maxdepth: 5

"""
    last_relative_part_0 = None
    last_relative_part_1 = None
    for json in all_jsons:
        relative = json.relative_to(pathlib.Path(root_dir) / 'doc' / 'examples')
        anchor = '-'.join(relative.parts)
        anchor = '.. _config-example-' + anchor.replace('.', '-') + ':\n\n'
        if len(relative.parts) > 0:
            part = relative.parts[0]
            if len(relative.parts) == 1:
                content += anchor
            if part != last_relative_part_0:
                content += len(part) * '*' + '\n'
                content += part + '\n'
                content += len(part) * '*' + '\n'
                content += '\n'
                last_relative_part_0 = part
        if len(relative.parts) > 1:
            part = relative.parts[1]
            if len(relative.parts) == 2:
                content += anchor
            if part != last_relative_part_1:
                content += part + '\n'
                content += len(part) * '=' + '\n'
                content += '\n'
                last_relative_part_1 = part
        if len(relative.parts) > 2:
            part = '/'.join(relative.parts[2:])
            content += anchor
            content += part + '\n'
            content += len(part) * '~' + '\n'
            content += '\n'
        content += '.. code-block:: json5\n'
        content += '\n'
        with open(json, 'r', encoding='utf-8') as f:
            indented = '\n'.join('    ' + line for line in f.read().splitlines())
            content += indented
            content += '\n\n'

    # Remove trailing spaces.
    content = '\n'.join(line.rstrip() for line in content.splitlines())

    with open('doc/sphinx/config-examples.rst', 'w', encoding='utf-8') as config_examples_rst:
        config_examples_rst.write(content)


if __name__ == '__main__':
    main()
